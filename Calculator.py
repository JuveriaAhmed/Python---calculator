name= input(" Enter your name: ")
weight = int(input("Enter your weight in pounds: "))
height = int(input("Enter your inches in inches: "))
BMI = (weight * 703) / (height  * height )
print(BMI)

if BMI >0:
    if(BMI < 18.5):
        print(name, "You are under weight")
    elif (BMI <= 24.9):
        print (name,"You are normal weight")
    elif (BMI < 29.9):
        print(name, "You are over weight")
    elif (BMI < 34.9):
        print(name, "you are Obease")    
    elif (BMI < 39.9):
        print(name, "You are severly obease")        
    elif (BMI > 39.9):
        print(name, "You are morbidly obease")
    else:
            print ("Enter valid inputs")


